    public static class VerbsUtils
    {
        private static Dictionary<string, Verbs> _stringMap = new Dictionary<string, Verbs>(StringComparer.OrdinalIgnoreCase);

        static VerbsUtils()
        {
            for (int i = 0; i < (int)Verbs._LAST; i++)
            {
                _stringMap.Add(((Verbs)i).ToString().Substring(2), (Verbs)i);
            }
        }

        public static Verbs ConvertStringToVerbs(string verb)
        {
            Verbs result;
            if (_stringMap.TryGetValue(verb, out result))
                return result;

            return Verbs.V_NONE;
        }

        public static bool IsTopicVerb(Verbs verb)
        {
            const Verbs first = Verbs.V_T_ABBE;
            const Verbs last = Verbs.V_T_WILKES;

            return verb >= first && verb <= last;
        }
    }
